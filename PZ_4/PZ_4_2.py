# Ввод данных (пример: можно заменить на статичные значения для тестирования)
A_input = input("\nВведите целое положительное число A (< B): ")
B_input = input("Введите целое положительное число B (> A): ")
# Используем try-except для обработки ошибок ввода
try:
    A = int(A_input)
    B = int(B_input)
    # Проверка условий задачи A и B > 0 и A < B
    if A <= 0 or B <= 0:
        print("Ошибка: A и B должны быть целыми положительными числами.")
    elif A >= B:
        print("Ошибка: A должно быть меньше B (A < B).")
    else:
        print("\nРезультат:")
        # Итерируемся от A до B включительно
        for num in range(A, B + 1):
            # Используем умножение строки на число для повторения
            print((str(num) + ' ') * num)
except ValueError as e:
    print(e)